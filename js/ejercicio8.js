// Meter dentro de la variable autor la respuesta que escriban en la ventana.
var autor = autor;

// Iniciar un bucle while para que se repita una instrucción mientras las respuesta sea distinta de "Cervantes"
while(autor!="Cervantes"){
    // Cualquier respuesta distinta de Cervantes sigue repitiendo el mensaje.
    autor = prompt("¿Quién es el autor de Don Quijote de la Mancha?");
}

